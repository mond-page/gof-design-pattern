package com.mond.gofdesignpattern.prototype;

public class GithubRepository {

    private String User;
    private String Name;

    public String getUser() {
        return User;
    }

    public void setUser(String user) {
        User = user;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }
}
